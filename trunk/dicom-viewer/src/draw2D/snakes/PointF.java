package draw2D.snakes;

public class PointF {
	public float x;
	public float y;
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public PointF(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
	public PointF() {
		super();
	}

}
